const idadeVinny = 36;
const idadeRaony = "32";

// operador igualdade
let comparacao = idadeVinny == idadeRaony;
console.log("Vinny tem a mesma idade? ", comparacao);

// operador diferença
comparacao = idadeVinny != idadeRaony;
console.log("Vinny tem uma idade diferente? ", comparacao);

// operador maior que
comparacao = idadeVinny > idadeRaony;
console.log("Vinny é mais velho? ", comparacao);

// operador menor que
comparacao = idadeVinny < idadeRaony;
console.log("Vinny é mais novo? ", comparacao);

// operador maior que ou igual a
comparacao = idadeVinny >= idadeRaony;
console.log("Vinny é mais velho ou tem a mesma idade? ", comparacao);

// operador menor que ou igual a
comparacao = idadeVinny <= idadeRaony;
console.log("Vinny é mais novo ou tem a mesma idade? ", comparacao);

// operador igualdade restrita
comparacao = idadeVinny === idadeRaony;
console.log("Vinny tem a mesma idade? ", comparacao);

// operador diferença restrita
comparacao = idadeVinny !== idadeRaony;
console.log("Vinny tem uma idade diferente? ", comparacao);
